def materialize_workspace_patch(draft: WorkspacePatchDraft) -> WorkspacePatch:
    return WorkspacePatch(
        id=draft.id.replace("draft_", "patch_", 1),
        workspace_id=draft.workspace_id,
        base_workspace_version=draft.base_workspace_version,
        operations=[
            WorkspaceOperation(type=operation.type, payload=operation.payload)
            for operation in draft.operations
        ],
        author=draft.author,
        message=f"Runtime PatchDraft prototype: {draft.instruction}".strip(),
        created_at=utc_now(),
    )
