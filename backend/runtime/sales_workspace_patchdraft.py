from __future__ import annotations

from typing import Any

from pydantic import BaseModel, ConfigDict, Field, field_validator

from backend.sales_workspace.schemas import SalesWorkspace, WorkspaceOperation, WorkspacePatch, utc_now


class RuntimeDraftModel(BaseModel):
    model_config = ConfigDict(extra="forbid")


class WorkspacePatchDraftOperation(RuntimeDraftModel):
    type: str
    payload: dict[str, Any] = Field(default_factory=dict)


class WorkspacePatchDraft(RuntimeDraftModel):
    id: str
    workspace_id: str
    base_workspace_version: int
    operations: list[WorkspacePatchDraftOperation]
    author: str = "runtime_patchdraft_prototype"
    instruction: str = ""
    runtime_metadata: dict[str, Any] = Field(default_factory=dict)

    @field_validator("operations")
    @classmethod
    def operations_must_not_be_empty(
        cls,
        value: list[WorkspacePatchDraftOperation],
    ) -> list[WorkspacePatchDraftOperation]:
        if not value:
            raise ValueError("WorkspacePatchDraft.operations must not be empty")
        return value


